package exercicio02;

public class exercicio02 {
    public static void main(String[] args) {

        int a = 10;
        int b = 9;

        System.out.println("A: "+a);
        System.out.println("B: "+b);

        if (a == b) {
            System.out.println("Os numeros de A e B são iguais.");
        } else {
            System.out.println("Os numeros de A e B não são iguais.");
        }
        if (a > b) {
            System.out.println("O numero da letra A é maior que o numero da letra B.");
        } else {
            System.out.println("O numero da letra B é maior que o numero da letra A.");
        }
        
    }
}